export function buildAccountMobileMenu(
  copy: AccountCenterCopy,
  hrefs: {
    accountPanel: string;
    dashboard: string;
    diagnosis?: string;
    faq: string;
    history: string;
    home: string;
    pricing: string;
    preferences: string;
    workspace: string;
  },
  variant: "account" | "workspace",
): AccountMobileMenuSection {
  const dashboardItem = {
    href: hrefs.dashboard,
    icon: "dashboard",
    label: copy.myData,
  };
  const preferencesItem = {
    href: hrefs.preferences,
    icon: "preferences",
    label: copy.actions.preferencesTitle,
  };
  const accountSettingsItem = {
    href: hrefs.accountPanel,
    icon: "settings",
    label: copy.settings,
  };
  if (variant === "account") {
    return {
      label: copy.menuLabel,
      items: [
        dashboardItem,
        { href: hrefs.workspace, icon: "tryon", label: copy.workspace },
        { href: hrefs.history, icon: "history", label: copy.fullHistory },
        preferencesItem,
        accountSettingsItem,
        { href: hrefs.pricing, icon: "membership", label: copy.memberCenter },
        { href: hrefs.faq, icon: "help", label: copy.helpCenter },
        {
          action: "logout",
          icon: "logout",
          label: copy.signOut,
          pendingLabel: copy.signingOut,
          failedLabel: copy.signOutFailed,
        },
      ],
    };
  }

  return {
    label: copy.menuLabel,
    items: [
      dashboardItem,
      { href: hrefs.history, icon: "history", label: copy.fullHistory },
      preferencesItem,
      accountSettingsItem,
      { href: hrefs.pricing, icon: "membership", label: copy.memberCenter },
      { href: hrefs.faq, icon: "help", label: copy.helpCenter },
      {
        action: "logout",
        icon: "logout",
        label: copy.signOut,
        pendingLabel: copy.signingOut,
        failedLabel: copy.signOutFailed,
      },
    ],
  };
}
