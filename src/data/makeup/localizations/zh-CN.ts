/** 构建本地化条目的辅助函数 */
function loc(
  marketVariantId: string,
  title: string,
  scenarios: string[],
  finishLabels: string[],
  experienceLabel: string,
  adv: LocalizedAdvisor,
): LookLocalization {
  return {
    marketVariantId,
    locale: "zh-CN",
    title,
    summary: `${title}，适合${scenarios.join("、")}场景，重点呈现${finishLabels.join("、")}妆效。`,
    imageAltTemplate: `${title}妆容参考图`,
    scenarios,
    finishLabels,
    experienceLabel,
    advisor: adv,
  };
}

/** 按 variantId 和 locale 查找本地化 */
export function getZhCNLocalization(
  marketVariantId: string,
): LookLocalization | undefined {
  return zhCNLocalizations.find((l) => l.marketVariantId === marketVariantId);
}
